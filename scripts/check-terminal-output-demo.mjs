#!/usr/bin/env node
import { spawnSync } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const writeMode = process.argv.includes("--write");
const assetPath = "docs/assets/terminal-output-demo.svg";
const passCommand = ["packages/cli/dist/bin.js", "check", "examples/passing-workflow.json"];
const failCommand = ["packages/cli/dist/bin.js", "check", "examples/failing-dead-parameter.json"];
const ansiPattern = /\u001B\[[0-9;]*m/;
const failures = [];

const pass = runCommand(passCommand);
const fail = runCommand(failCommand);

expect(pass.status === 0, "pass demo command must exit with status 0");
expect(fail.status === 1, "fail demo command must exit with status 1");
expect(hasAnsi(pass.output), "pass demo output must include ANSI color when FORCE_COLOR=1");
expect(hasAnsi(fail.output), "fail demo output must include ANSI color when FORCE_COLOR=1");
expect(pass.output.includes("\u001B[32mPASS\u001B[0m"), "pass demo must include a green PASS status");
expect(fail.output.includes("\u001B[31mFAIL\u001B[0m"), "fail demo must include a red FAIL status");
expect(fail.output.includes("\u001B[31mERROR\u001B[0m"), "fail demo must include a red ERROR status");
expect(fail.output.includes("notARealParameter"), "fail demo must include the dead parameter name");
expect(
  fail.output.includes("$.nodes[0].parameters.notARealParameter"),
  "fail demo must include line-level node path detail"
);
expect(pass.output.includes("Summary: 1 passed, 0 failed"), "pass demo must include a passing summary");
expect(fail.output.includes("Summary: 0 passed, 1 failed"), "fail demo must include a failing summary");
expect(
  pass.output.includes("not live REST validation") && fail.output.includes("not live REST validation"),
  "terminal demo must preserve the live REST boundary warning"
);

if (failures.length > 0) {
  throw new Error(`terminal output demo check failed:\n${failures.map((failure) => `- ${failure}`).join("\n")}`);
}

const expectedSvg = renderAsset({
  pass: {
    title: "Pass output",
    command: `node ${passCommand.join(" ")}`,
    output: pass.output
  },
  fail: {
    title: "Fail output",
    command: `node ${failCommand.join(" ")}`,
    output: fail.output
  }
});

if (writeMode) {
  await mkdir(path.dirname(assetPath), { recursive: true });
  await writeFile(assetPath, expectedSvg, "utf8");
} else {
  const actualSvg = await readFile(assetPath, "utf8");
  if (actualSvg !== expectedSvg) {
    throw new Error(
      `terminal output demo asset is stale. Run npm run generate:terminal-output-demo and commit ${assetPath}.`
    );
  }
}

console.log(
  JSON.stringify(
    {
      ok: true,
      asset: assetPath,
      commands: [`node ${passCommand.join(" ")}`, `node ${failCommand.join(" ")}`],
      checked: [
        "real colored pass CLI output",
        "real colored fail CLI output",
        "dead-parameter line-level detail",
        "live REST boundary warning",
        "checked terminal SVG asset"
      ]
    },
    null,
    2
  )
);

function runCommand(command) {
  const result = spawnSync(process.execPath, command, {
    cwd: process.cwd(),
    encoding: "utf8",
    env: {
      ...process.env,
      FORCE_COLOR: "1",
      NO_COLOR: undefined
    }
  });

  return {
    status: result.status,
    output: normalizeLines([result.stdout, result.stderr].filter(Boolean).join("\n"))
  };
}

function renderAsset({ pass, fail }) {
  const panels = [pass, fail].map((panel) => ({
    ...panel,
    lines: [`$ ${panel.command}`, ...panel.output.split("\n")].flatMap((line) => wrapAnsiLine(line, 64))
  }));
  const width = 1360;
  const panelWidth = 622;
  const panelHeight = Math.max(...panels.map((panel) => 96 + panel.lines.length * 22 + 36));
  const height = 156 + panelHeight;

  return `${[
    `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}" role="img" aria-labelledby="title desc">`,
    '  <title id="title">n8n-lint terminal output proof</title>',
    '  <desc id="desc">Generated from actual colored n8n-lint pass and fail CLI output.</desc>',
    '  <rect width="100%" height="100%" fill="#f8fafc"/>',
    '  <text x="48" y="62" fill="#0f172a" font-family="Inter, Segoe UI, Arial, sans-serif" font-size="32" font-weight="800">Terminal proof from real CLI output</text>',
    '  <text x="48" y="96" fill="#475569" font-family="Inter, Segoe UI, Arial, sans-serif" font-size="16">Both panels are regenerated by npm run generate:terminal-output-demo from the built CLI with FORCE_COLOR=1.</text>',
    renderPanel(panels[0], 48, 126, panelWidth, panelHeight, "#16a34a"),
    renderPanel(panels[1], 690, 126, panelWidth, panelHeight, "#dc2626"),
    "</svg>",
    ""
  ].join("\n")}`;
}

function renderPanel(panel, x, y, width, height, accentColor) {
  const rows = panel.lines.map((line, index) => renderAnsiLine(line, x + 24, y + 84 + index * 22)).join("\n");

  return [
    `  <rect x="${x}" y="${y}" width="${width}" height="${height}" rx="12" fill="#0f172a" stroke="#cbd5e1"/>`,
    `  <rect x="${x}" y="${y}" width="${width}" height="46" rx="12" fill="#111827"/>`,
    `  <rect x="${x}" y="${y + 34}" width="${width}" height="12" fill="#111827"/>`,
    '  <circle cx="' + (x + 24) + '" cy="' + (y + 24) + '" r="6" fill="#ef4444"/>',
    '  <circle cx="' + (x + 44) + '" cy="' + (y + 24) + '" r="6" fill="#f59e0b"/>',
    '  <circle cx="' + (x + 64) + '" cy="' + (y + 24) + '" r="6" fill="#22c55e"/>',
    `  <text x="${x + 92}" y="${y + 30}" fill="#cbd5e1" font-family="Inter, Segoe UI, Arial, sans-serif" font-size="14">${escapeXml(panel.title)}</text>`,
    `  <rect x="${x + 24}" y="${y + 57}" width="10" height="10" rx="5" fill="${accentColor}"/>`,
    `  <text x="${x + 42}" y="${y + 67}" fill="#94a3b8" font-family="Inter, Segoe UI, Arial, sans-serif" font-size="13">actual command output</text>`,
    `  <g font-family="Consolas, Menlo, Monaco, 'Liberation Mono', monospace" font-size="14">`,
    rows,
    "  </g>"
  ].join("\n");
}

function renderAnsiLine(line, x, y) {
  const segments = parseAnsiSegments(line);
  let cursor = x;
  const spans = [];

  for (const segment of segments) {
    const text = escapeXml(segment.text);
    spans.push(`<tspan x="${cursor}" dy="0" fill="${segment.color}">${text}</tspan>`);
    cursor += segment.text.length * 8.2;
  }

  return `    <text x="${x}" y="${y}">${spans.join("")}</text>`;
}

function parseAnsiSegments(line) {
  const segments = [];
  let currentColor = "#e5e7eb";
  let cursor = 0;
  const pattern = /\u001B\[([0-9;]*)m/g;
  let match;

  while ((match = pattern.exec(line)) !== null) {
    if (match.index > cursor) {
      segments.push({ text: line.slice(cursor, match.index), color: currentColor });
    }

    currentColor = colorForAnsi(match[1] ?? "", currentColor);
    cursor = pattern.lastIndex;
  }

  if (cursor < line.length) {
    segments.push({ text: line.slice(cursor), color: currentColor });
  }

  return segments.length === 0 ? [{ text: line, color: currentColor }] : segments;
}

function colorForAnsi(code, currentColor) {
  if (code === "0" || code === "") {
    return "#e5e7eb";
  }

  const codes = code.split(";");
  if (codes.includes("31")) {
    return "#f87171";
  }

  if (codes.includes("32")) {
    return "#22c55e";
  }

  if (codes.includes("33")) {
    return "#facc15";
  }

  if (codes.includes("36")) {
    return "#67e8f9";
  }

  return currentColor;
}

function wrapAnsiLine(line, maxLength) {
  const plain = stripAnsi(line);
  if (plain.length <= maxLength) {
    return [line];
  }

  return wrapPlainLine(plain, maxLength).map((wrappedLine) => colorizeWrappedLine(wrappedLine, line));
}

function colorizeWrappedLine(wrappedLine, sourceLine) {
  const stripped = stripAnsi(sourceLine);
  const leading = stripped.trimStart();
  if (leading.startsWith("PASS")) {
    return `\u001B[32m${wrappedLine}\u001B[0m`;
  }

  if (leading.startsWith("FAIL") || leading.startsWith("ERROR")) {
    return `\u001B[31m${wrappedLine}\u001B[0m`;
  }

  if (leading.startsWith("WARN") || leading.startsWith("WARNING")) {
    return `\u001B[33m${wrappedLine}\u001B[0m`;
  }

  if (leading.startsWith("$")) {
    return `\u001B[36m${wrappedLine}\u001B[0m`;
  }

  return wrappedLine;
}

function wrapPlainLine(line, maxLength) {
  if (line.length <= maxLength) {
    return [line];
  }

  const chunks = [];
  let remaining = line;
  while (remaining.length > maxLength) {
    const breakAt = remaining.lastIndexOf(" ", maxLength);
    const index = breakAt > 0 ? breakAt : maxLength;
    chunks.push(remaining.slice(0, index));
    remaining = `  ${remaining.slice(index).trimStart()}`;
  }
  chunks.push(remaining);
  return chunks;
}

function stripAnsi(text) {
  return text.replace(/\u001B\[[0-9;]*m/g, "");
}

function hasAnsi(text) {
  return ansiPattern.test(text);
}

function normalizeLines(text) {
  return text.replace(/\r\n/g, "\n").trimEnd();
}

function escapeXml(text) {
  return String(text)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function expect(condition, message) {
  if (!condition) {
    failures.push(message);
  }
}
